/// 字体打包导出器
///
/// 将字体项目打包为包含多种格式的 ZIP：
/// - TTF 字体文件
/// - WOFF 字体文件
/// - CSS @font-face 代码片段
/// - 字体样本图片（不同字号的字符渲染）
class FontPackageExporter {
  FontPackageExporter._();

  /// 导出字体项目为打包 ZIP 字节
  ///
  /// 返回 ZIP 文件的二进制数据。
  static Future<Uint8List> exportPackage(FontProject project) async {
    final metadata = project.metadata;
    final familyName = metadata?.familyName ?? project.name;
    final safeName = familyName.replaceAll(RegExp(r'[^\w\-]'), '_');

    // 1. 构建 TTF
    final glyphs = project.glyphs.values.toList();
    final builder = TtfBuilder(
      glyphs: glyphs,
      familyName: project.name,
      unitsPerEm: 1000,
      kerningPairs: project.kerningPairs,
      customFamilyName: familyName,
      customSubfamilyName: metadata?.subfamilyName ?? 'Regular',
      customVersion: metadata?.version ?? 'Version 1.0',
      customCopyright: metadata?.copyright ?? '',
      customDescription: metadata?.description ?? '',
    );
    final ttfBytes = builder.build();

    // 2. 构建 WOFF
    final woffBuilder = WoffBuilder(ttfBytes: ttfBytes);
    final woffBytes = woffBuilder.build();

    // 3. 生成 CSS @font-face
    final cssContent = _generateCss(familyName, safeName);
    final cssBytes = utf8.encode(cssContent);

    // 4. 生成字体样本图片
    final samplePngBytes = await _generateSampleImage(
      ttfBytes: ttfBytes,
      familyName: familyName,
      sampleChars: _pickSampleChars(glyphs),
    );

    // 5. 生成 HTML 预览页
    final htmlContent = _generateHtmlPreview(familyName, safeName);
    final htmlBytes = utf8.encode(htmlContent);

    // 6. 打包 ZIP
    final archive = Archive();

    archive.addFile(ArchiveFile('$safeName.ttf', ttfBytes.length, ttfBytes));
    archive.addFile(ArchiveFile('$safeName.woff', woffBytes.length, woffBytes));
    archive.addFile(ArchiveFile('$safeName.css', cssBytes.length, cssBytes));
    archive.addFile(ArchiveFile('preview.html', htmlBytes.length, htmlBytes));
    if (samplePngBytes != null) {
      archive.addFile(ArchiveFile(
        '${safeName}_sample.png',
        samplePngBytes.length,
        samplePngBytes,
      ));
    }

    // README
    final readmeBytes = utf8.encode(_generateReadme(familyName));
    archive.addFile(ArchiveFile('README.txt', readmeBytes.length, readmeBytes));

    return Uint8List.fromList(ZipEncoder().encode(archive)!);
  }

  /// 生成 CSS @font-face 代码
  static String _generateCss(String familyName, String safeName) {
    return '''/* WriteFont 字体导出 — $familyName */
/* Generated by WriteFont v2.3.0 */

@font-face {
  font-family: '$familyName';
  src: url('$safeName.ttf') format('truetype'),
       url('$safeName.woff') format('woff');
  font-weight: normal;
  font-style: normal;
  font-display: swap;
}

/* 使用示例 */
/*
body {
  font-family: '$familyName', sans-serif;
}
*/
''';
  }

