  /// 选取样本字符（取前几个有轮廓的字符）
  static List<String> _pickSampleChars(List<GlyphData> glyphs) {
    final withContours = glyphs.where((g) => g.contours.isNotEmpty).toList();
    final chars = <String>[];
    for (final g in withContours.take(8)) {
      chars.add(g.character);
    }
    // 如果字符太少，补充默认字符
    if (chars.isEmpty) {
      chars.addAll(['字', '体', '预', '览']);
    }
    return chars;
  }

  /// 生成字体样本图片
  ///
  /// 使用 Canvas 绘制不同字号的字符，返回 PNG 字节。
  /// 由于 dart:ui 的 FontLoader 在 isolate 中不可用，
  /// 此方法生成一个简单的文字样本图片。
  static Future<Uint8List?> _generateSampleImage({
    required Uint8List ttfBytes,
    required String familyName,
    required List<String> sampleChars,
  }) async {
    try {
      const width = 800;
      const height = 400;
      final recorder = PictureRecorder();
      final canvas = Canvas(recorder);

      // 背景
      final bgPaint = Paint()..color = const Color(0xFFF8F9FA);
      canvas.drawRect(
        Rect.fromLTWH(0, 0, width.toDouble(), height.toDouble()),
        bgPaint,
      );

      // 标题
      final titlePainter = TextPainter(
        text: TextSpan(
          text: familyName,
          style: const TextStyle(
            fontSize: 32,
            fontWeight: FontWeight.bold,
            color: Color(0xFF333333),
          ),
        ),
        textDirection: TextDirection.ltr,
      );
      titlePainter.layout();
      titlePainter.paint(canvas, const Offset(40, 30));

      // 副标题
      final subtitlePainter = TextPainter(
        text: const TextSpan(
          text: 'WriteFont 字体样本',
          style: TextStyle(
            fontSize: 14,
            color: Color(0xFF999999),
          ),
        ),
        textDirection: TextDirection.ltr,
      );
      subtitlePainter.layout();
      subtitlePainter.paint(canvas, const Offset(40, 72));

      // 分隔线
      final linePaint = Paint()
        ..color = const Color(0xFFE0E0E0)
        ..strokeWidth = 1;
      canvas.drawLine(
        const Offset(40, 95),
        const Offset(760, 95),
        linePaint,
      );

      // 样本字符（不同字号）
      final sizes = [48.0, 36.0, 24.0, 18.0];
      double y = 110;
      final sampleText = sampleChars.join('  ');

      for (final size in sizes) {
        if (y > height - 50) break;
        final painter = TextPainter(
          text: TextSpan(
            text: sampleText,
            style: TextStyle(
              fontSize: size,
              color: const Color(0xFF333333),
            ),
          ),
          textDirection: TextDirection.ltr,
          maxLines: 1,
        );
        painter.layout(maxWidth: 720);
        painter.paint(canvas, Offset(40, y));
        y += size + 16;
      }

      // 底部信息
      final infoPainter = TextPainter(
        text: TextSpan(
          text: 'Generated by WriteFont · ${DateTime.now().year}',
          style: const TextStyle(
            fontSize: 11,
            color: Color(0xFFBBBBBB),
          ),
        ),
        textDirection: TextDirection.ltr,
      );
      infoPainter.layout();
      infoPainter.paint(
        canvas,
        Offset(40, height - 30.0),
      );

      final picture = recorder.endRecording();
      final image = await picture.toImage(width, height);
      final byteData = await image.toByteData(format: ImageByteFormat.png);
      image.dispose();

      if (byteData != null) {
        return byteData.buffer.asUint8List();
      }
      return null;
    } catch (e) {
      // 样本图片生成失败不影响打包
      return null;
    }
  }
}
