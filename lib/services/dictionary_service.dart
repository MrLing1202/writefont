  /// 获取字符的频率排名（0 = 最常用，越大越不常用）
  /// 返回 -1 表示不在频率表中（非常用字）
  int getFrequency(String char) {
    if (char.isEmpty) return -1;
    final index = _frequencyTable.indexOf(char);
    return index >= 0 ? index : -1;
  }

  /// 判断是否为常见字（在前 3000 常用字表中）
  bool isCommonChar(String char) {
    if (char.isEmpty) return false;
    return _frequencyTable.contains(char);
  }

  /// 判断是否为用户常用字
  bool isUserCommonChar(String char) {
    if (char.isEmpty) return false;
    return _userCharFrequency.containsKey(char);
  }

  /// 获取形近的常见字列表
  ///
  /// 返回与 [char] 形近的常见字列表（空列表表示无形近字映射）
  List<String> getSimilarChars(String char) {
    if (char.isEmpty) return [];
    return _similarChars[char] ?? [];
  }

  /// 推荐相似的常见字
  ///
  /// 逻辑：
  /// 1. 如果 char 本身是常见字，直接返回 char
  /// 2. 查找形近字表，返回其中频率最高的常见字
  /// 3. 无形近字则返回 null
  String? suggestSimilar(String char) {
    if (char.isEmpty) return null;

    // 本身是常见字，无需替换
    if (isCommonChar(char)) return char;

    // 查找形近字
    final similar = getSimilarChars(char);
    if (similar.isEmpty) return null;

    // 选择频率最高的形近字
    String? best;
    int bestRank = _frequencyTable.length + 1;
    for (final s in similar) {
      final rank = getFrequency(s);
      if (rank >= 0 && rank < bestRank) {
        bestRank = rank;
        best = s;
      }
    }

    return best;
  }

  /// 后处理识别结果
  ///
  /// 核心方法：接收原始识别结果，返回优化后的结果。
  ///
  /// 逻辑：
  /// 1. 如果识别结果是常见字 → 直接返回
  /// 2. 如果有形近常见字 → 返回最常见那个
  /// 3. 如果用户历史常用这个字 → 返回原字
  /// 4. 否则返回原字（不替换）
  ///
  /// [result] 原始识别结果字符
  /// [confidence] 识别置信度（0.0~1.0），仅当置信度不太低时才做替换
  String postProcess(String result, {double confidence = 1.0}) {
    if (result.isEmpty) return result;

    final char = result;

    // 常见字，无需处理
    if (isCommonChar(char)) return char;

    // 置信度太低时不做替换，避免误改
    if (confidence < 0.5) return char;

    // 用户历史中常用此字，保留
    if (isUserCommonChar(char)) return char;

    // 查找形近常见字
    final suggested = suggestSimilar(char);
    if (suggested != null && suggested != char) {
      // 只有置信度不够高时才替换（≥0.5 且 < 0.85）
      // 高置信度时信任 OCR 结果
      if (confidence < 0.85) {
        debugPrint('字典后处理: "$char" → "$suggested" (形近字替换, 置信度=${(confidence * 100).toStringAsFixed(0)}%)');
        return suggested;
      }
    }

    return char;
  }

  /// 同音字上下文纠错
  ///
  /// 当置信度 < 0.85 时，检查识别结果的同音字是否能与前后字符组成更合理的词。
  /// 例如：识别为"相"，前一字"想"，后一字"念" → "想念"是词，"相念"不是 → 不替换
  /// 但：识别为"相"，后一字"信" → "相信"是词 → 保留"相"
  ///
  /// [result] 原始识别结果
  /// [prevChar] 前一个字符（可选）
  /// [nextChar] 后一个字符（可选）
  /// [confidence] 识别置信度
  String correctWithHomophone(String result, {String? prevChar, String? nextChar, double confidence = 1.0}) {
    if (result.isEmpty) return result;

    // 高置信度时不做同音字纠错
    if (confidence >= 0.85) return result;

    // 置信度太低时不做替换，避免误改
    if (confidence < 0.3) return result;

    final homophones = _homophones[result];
    if (homophones == null || homophones.isEmpty) return result;

    // 检查当前字符与上下文是否已能组成词
    bool currentFitsContext = false;
    if (nextChar != null && nextChar.isNotEmpty) {
      currentFitsContext = _commonWords.contains('$result$nextChar');
    }
    if (!currentFitsContext && prevChar != null && prevChar.isNotEmpty) {
      currentFitsContext = _commonWords.contains('$prevChar$result');
    }

    // 如果当前字符已经能组成词，不做替换
    if (currentFitsContext) return result;

    // 尝试每个同音字，找到能与上下文组成词的
    for (final candidate in homophones) {
      if (candidate == result) continue;

      // 检查候选字 + 下一字是否为词
      if (nextChar != null && nextChar.isNotEmpty && _commonWords.contains('$candidate$nextChar')) {
        debugPrint('同音字纠错: "$result" → "$candidate" (后文词组: "$candidate$nextChar", 置信度=${(confidence * 100).toStringAsFixed(0)}%)');
        return candidate;
      }

      // 检查前一字 + 候选字是否为词
      if (prevChar != null && prevChar.isNotEmpty && _commonWords.contains('$prevChar$candidate')) {
        debugPrint('同音字纠错: "$result" → "$candidate" (前文词组: "$prevChar$candidate", 置信度=${(confidence * 100).toStringAsFixed(0)}%)');
        return candidate;
      }
    }

    return result;
  }

  /// 记录用户识别过的字符（更新用户常用字缓存）
  ///
  /// 在每次识别成功后调用，累积用户的使用习惯。
  Future<void> recordUsage(String char) async {
    if (char.isEmpty) return;

    await _ensureLoaded();

    _userCharFrequency[char] = (_userCharFrequency[char] ?? 0) + 1;

    // 限制缓存大小，淘汰使用次数最少的
    if (_userCharFrequency.length > _maxUserChars) {
      _pruneUserChars();
    }

    await _save();
  }

  /// 获取用户使用频率最高的字符列表
  List<MapEntry<String, int>> getTopUserChars({int limit = 50}) {
    final sorted = _userCharFrequency.entries.toList()
      ..sort((a, b) => b.value.compareTo(a.value));
    return sorted.take(limit).toList();
  }

  /// 获取字典服务统计信息
  Map<String, dynamic> getStats() {
    return {
      'frequencyTableSize': _frequencyTable.length,
      'similarCharGroups': _similarChars.length,
      'userCharCount': _userCharFrequency.length,
      'topUserChars': getTopUserChars(limit: 10).map((e) => '${e.key}(${e.value})').toList(),
    };
  }

  /// 清空用户常用字缓存
  Future<void> clearUserChars() async {
    _userCharFrequency.clear();
    final prefs = await SharedPreferences.getInstance();
    await prefs.remove(_prefKeyUserChars);
    debugPrint('字典服务: 已清空用户常用字缓存');
  }

